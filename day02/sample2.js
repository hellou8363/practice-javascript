// 비교 연산자
const str = '10';
const num = 10;

console.log("==를 사용한 비교");
if(str == num) {
  console.log(`str: ${str}과 num: ${num}은 같습니다.`);
} else {
  console.log(`str: ${str}과 num: ${num}은 다릅니다.`);
} // if-else

console.log("===를 사용한 비교");
if(str === num) {
  console.log(`str: ${str}과 num: ${num}은 같습니다.`);
} else {
  console.log(`str: ${str}과 num: ${num}은 다릅니다.`);
} // if-else

